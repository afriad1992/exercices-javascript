var magic = parseInt(Math.random()*100);

do{var nb=parseInt(prompt("donnez un nombre magique"));
{if (nb<magic){console.log("plus petit");}
                else if(nb>magic) {console.log("plus grand");}
            }}while(nb!=magic)
if (nb==magic){console.log("vous avez trouvez le nombre magic ");}
